function diaDaSemana(dia) {
    switch (dia) {
        case 1:
            console.log("Domingo: Fim de semana")
            break
        case 2:
            console.log("Segunda-feira: Dia últil")
            break
        case 3:
            console.log("Terça-Feira: Dia últil")
            break
        case 4:
            console.log("Quarta-Feira: Dia últil")
            break
        case 5:
            console.log("Quinta-Feira: Dia últil")
            break
        case 6:
            console.log("Sexta-feira: Dia últil")
            break
        case 7:
            console.log("Sábado: Fim de semana")
            break
            default:
                console.log("Dia inválido!")
        } 

}

diaDaSemana(1)